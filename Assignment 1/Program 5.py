# print day 0 - 6 day start from monday

day = int(input("Enter Day 0 - 6 To check day : "))

if day == 0:
    print("Monday")
elif day == 1:
    print("Tuesday")
elif day == 2:
    print("Wednesday")
elif day == 3:
    print("Thursday")
elif day == 4:
    print("Friday")
elif day == 5:
    print("Saturday")
elif day == 6:
    print("Sunday")
else:
    print("Invalid input Enter Number Between 0 - 6 ")